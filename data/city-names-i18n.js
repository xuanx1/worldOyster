    /**
     * Translate a city name into the active UI language.
     * Falls back to English name when no translation exists.
     * @param {string} name — canonical (English) city name
     * @param {string} [lang] — override language code; defaults to window.i18n.lang
     * @returns {string}
     */
    window.translateCity = function (name, lang) {
        if (!name) return '';
        var l = lang || (window.i18n && window.i18n.lang) || 'en';
        if (l === 'en') return name;
        var entry = C[name];
        return (entry && entry[l]) || name;
    };
