    /**
     * Translate a country / territory name.
     * Falls back to the original English name when no translation exists.
     * @param {string} name — English country name (as used in countries.js)
     * @param {string} [lang] — Target language code; defaults to current i18n language
     * @returns {string}
     */
    function translateCountry(name, lang) {
        if (!name) return '';
        const l = lang || (window.i18n ? window.i18n.getLang() : 'en');
        if (l === 'en') return name;
        const entry = C[name];
        return (entry && entry[l]) || name;
    }

